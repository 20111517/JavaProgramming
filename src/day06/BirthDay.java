package day06;

public class BirthDay {
    public static void main(String[] args) {
         String name="Mustafa";
         int birtDay=11;
         String birthMonth="July";
         int birthYear=1986;
        System.out.println(name+" was born on "+birthMonth+"/"+birtDay+"/"+birthYear+".");
    }
}
