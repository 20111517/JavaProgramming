package day_15Loop;

public class ForLopPractises {

    public static void main(String[] args) {
        for (int i = 15; i <=45 ; i++) {
            System.out.print(i+" ");
        }

        for (int i = 100; i >=50 ; i--) {
            System.out.print(i+" ");
        }
        // print all the even numbers between 1 ~ 55

        for (int i = 1; i <=55 ; i++) {
            if(i%2==0)
                System.out.println(i);
        }
        for (int i = 2; i <=55 ; i+=2) {
            System.out.println(i+" ");
        }








    }







}
