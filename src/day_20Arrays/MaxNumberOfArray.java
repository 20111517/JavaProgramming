package day_20Arrays;

public class MaxNumberOfArray {

    public static void main(String[] args) {
        int[]numbers={12,25,26,4,35,87};
        int max=numbers[0];
        for (int i = 0; i < numbers.length; i++) {
            if(numbers[i]>max);
            max=numbers[i];
        }
        System.out.println(max);

    }









}
